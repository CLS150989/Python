numero1 = int(input('escriba un número: '))
numero2 = int(input('escriba otro número: '))

if(numero1 == numero2): #operador de igualdad
    print("el numero %d es igual al número %d "  %(numero1, numero2))
if(numero1 !=numero2): #operador de difeencia
    print("el número %d es diferente al número %d" %(numero1, numero2))
if(numero1 < numero2): #operador mayor que
    print("el numero %d es menor al número %d" %(numero1, numero2))
if(numero1 > numero2): # operador menor que 
    print("el numero %d es mayor al numero %d" %(numero1, numero2))    
if (numero1 >= numero2): #operador mayor o igual que 
    print("el numero %d es mayor o igual que el numero %d" %(numero1, numero2))
if (numero1 <= numero2): #operador menor o igual que 
    print("el numero %d es menor o igual que %d" %(numero1, numero2))